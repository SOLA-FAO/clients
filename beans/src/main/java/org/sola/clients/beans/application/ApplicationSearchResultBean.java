package org.sola.clients.beans.application;

import org.sola.clients.beans.cache.CacheManager;
import org.sola.clients.beans.referencedata.ApplicationStatusTypeBean;
import org.sola.webservices.transferobjects.search.ApplicationSearchResultTO;

/**
 * Represents search result of applications. Could be populated from the
 * {@link ApplicationSearchResultTO} object.<br />
 */
public class ApplicationSearchResultBean extends ApplicationSummaryBean {

    public static final String CHECKED_PROPERTY = "checked";
    public static final String STATUS_BEAN_PROPERTY = "statusBean";

    private transient String status;
    private ApplicationStatusTypeBean statusBean;
    private String assigneeId;
    private String assigneeName;
    private String contactPerson;
    private String agent;
    private String serviceList;
    private String serviceDescriptions;
    private boolean checked;

    public ApplicationSearchResultBean() {
        super();
    }

    public String getAgent() {
        return agent;
    }

    public void setAgent(String agent) {
        this.agent = agent;
    }

    public String getAssigneeId() {
        return assigneeId;
    }

    public void setAssigneeId(String assigneeId) {
        this.assigneeId = assigneeId;
    }

    public String getAssigneeName() {
        return assigneeName;
    }

    public void setAssigneeName(String assigneeName) {
        this.assigneeName = assigneeName;
    }

    public String getContactPerson() {
        return contactPerson;
    }

    public void setContactPerson(String contactPerson) {
        this.contactPerson = contactPerson;
    }

    public String getServiceList() {
        return serviceList;
    }

    public void setServiceList(String serviceList) {
        this.serviceList = serviceList;
    }

    public boolean isChecked() {
        return checked;
    }

    public void setChecked(boolean checked) {
        boolean oldValue = this.checked;
        this.checked = checked;
        propertySupport.firePropertyChange(CHECKED_PROPERTY, oldValue, this.checked);
    }

    public ApplicationStatusTypeBean getStatusBean() {
        return statusBean;
    }

    public void setStatusBean(ApplicationStatusTypeBean statusBean) {
        if (this.statusBean == null) {
            this.statusBean = new ApplicationStatusTypeBean();
        }
        this.setJointRefDataBean(this.statusBean, statusBean, STATUS_BEAN_PROPERTY);
    }

    public String getStatus() {
        return statusBean == null ? null : statusBean.getCode();
    }

    public void setStatus(String value) {
        setStatusBean(CacheManager.getBeanByCode(
                CacheManager.getApplicationStatusTypes(), value));
    }

    public String getServiceDescriptions() {
        return serviceDescriptions;
    }

    public void setServiceDescriptions(String serviceDescriptions) {
        this.serviceDescriptions = serviceDescriptions;
    }
}
